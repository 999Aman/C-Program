#include <stdio.h>

int main() {
  int x = 5;
  int y = 3;
 
 // Operator ==	Equal to	x == y
 printf("%d \n", x == y);
// Operator !=	Not equal	x != y	
printf("%d \n", x != y);
// Operator >	Greater than	x > y	
printf("%d \n", x > y);

// Operator <	Less than	x < y	
printf("%d \n", x < y);
// Operator >=	Greater than or equal to	x >= y	
printf("%d \n", x >= y);
// Operator <=	Less than or equal to	x <= y
printf("%d \n", x <= y);
return 0;
}